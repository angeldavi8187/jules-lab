def sumar(a, b):
    """
    Esta función toma dos números como entrada y devuelve su suma.

    Args:
        a: El primer número.
        b: El segundo número.

    Returns:
        La suma de a y b.
    """
    return a + b

def restar(a, b):
    """
    Esta función toma dos números como entrada y devuelve la resta de a menos b.

    Args:
        a: El primer número (minuendo).
        b: El segundo número (sustraendo).

    Returns:
        La diferencia de a menos b.
    """
    return a - b

